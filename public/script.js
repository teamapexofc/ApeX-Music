// Simulated bot stats - in production, fetch from your API
function updateStats() {
    // Simulate realistic numbers
    const baseServers = 752;
    const baseUsers = 89420;
    const baseCommands = 47;
    const basePlayers = 143;
    
    // Add some randomness for realism
    const servers = baseServers + Math.floor(Math.random() * 50);
    const users = baseUsers + Math.floor(Math.random() * 5000);
    const commands = baseCommands;
    const players = basePlayers + Math.floor(Math.random() * 30);
    
    // Animate the numbers
    animateValue('serverCount', 0, servers, 2000);
    animateValue('userCount', 0, users, 2000);
    animateValue('commandCount', 0, commands, 1500);
    animateValue('playerCount', 0, players, 2000);
}

// Animate number counting
function animateValue(id, start, end, duration) {
    const element = document.getElementById(id);
    if (!element) return;
    
    const range = end - start;
    const increment = range / (duration / 16);
    let current = start;
    
    const timer = setInterval(() => {
        current += increment;
        if (current >= end) {
            element.textContent = end.toLocaleString();
            clearInterval(timer);
        } else {
            element.textContent = Math.floor(current).toLocaleString();
        }
    }, 16);
}

// Smooth scroll for navigation links
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function (e) {
        e.preventDefault();
        const target = document.querySelector(this.getAttribute('href'));
        if (target) {
            target.scrollIntoView({
                behavior: 'smooth',
                block: 'start'
            });
        }
    });
});

// Add scroll-based animations
const observerOptions = {
    threshold: 0.1,
    rootMargin: '0px 0px -100px 0px'
};

const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.style.opacity = '1';
            entry.target.style.transform = 'translateY(0)';
        }
    });
}, observerOptions);

